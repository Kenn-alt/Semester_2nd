# type casting 
x = 5.4
y = 4
z = "3"

print(int(x)) # out: 5
print(int(y)) # out: 4
print(int(z)) # out: 3


x = 5.4
y = 4
z = "3"
t = '4.2'

print(float(x)) # out: 5.4
print(float(y)) # out: 4.0
print(float(z)) # out: 3.0
print(float(t)) # out: 4.2

x = 's1'
y = 3
z = 4.2

print(str(x)) # out: s1
print(str(y)) # out: 3
print(str(z)) # out: 4.2